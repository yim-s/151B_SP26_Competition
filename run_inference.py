def options_in_FRQ(question: str) -> bool:
    return bool(re.search(r"\bA[\.\)]\s+", question) and re.search(r"\bB[\.\)]\s+", question))

def number_ANS(question: str) -> int:
    return question.count("[ANS]")

def build_prompt(question: str, options: Optional[list]) -> tuple[str, str]:
    """Return (system_prompt, user_prompt) for a question."""
    if options:
        labels    = [chr(65 + i) for i in range(len(options))]
        opts_text = "\n".join(f"{lbl}. {opt.strip()}" for lbl, opt in zip(labels, options))
        user_prompt_MCQ = (
            f"{question}\n\nOptions:\n{opts_text}"
        )
        return SYSTEM_PROMPT_MCQ, user_prompt_MCQ

    n_ANS = number_ANS(question)

    if options_in_FRQ(question): #Q6, Q80
        user_prompt_EMBEDDED_MCQ = (
            f"{question}\n\n"
            f"This problem has {n_ANS} [ANS] placeholder{'s' if n_ANS != 1 else ''}; each should be filled by a single uppercase letter chosen from the options listed inside the question."
        )
        return SYSTEM_PROMPT_EMBEDDED_MCQ, user_prompt_EMBEDDED_MCQ

    expected = max(n_ANS, 1)

    user_prompt_FRQ = (
        f"{question}\n\n"
        f"This problem expects exactly {expected} answer{'s' if expected != 1 else ''} inside \\boxed{{}}."
    )
    return SYSTEM_PROMPT_MATH, user_prompt_FRQ


