def extract_ANS(text: str):
    if "\\boxed{" not in text:
        return []
    try:
        ans = judger.extract_boxed_answer(text)
        if not ans or ans == text:
            return []
        ans_list = judger.split_by_comma(ans)
        return ans_list
    except:
        return []


def normalize_bool(ans_field):
    ans = ans_field.strip()
    if ans in TRUE:  return "T"
    if ans in FALSE: return "F"
    if ans in YES:  return "yes"
    if ans in NO: return "no"
    return None


def coerce(ans_field, is_mcq=False):
    ans = ans_field.strip()
    if not ans:
        return ans_field

    if is_mcq:
        choice = re.search(r"\b([A-Z])\b", ans)
        return choice.group(1) if choice else ans

    bool_norm = normalize_bool(ans)
    if bool_norm is not None:
        return bool_norm

    if DECIMAL.match(ans):
        return ans

    if LETTER.match(ans):
        return ans

    try:
        expr = latex2sympy(ans)
        if hasattr(expr, "free_symbols") and not expr.free_symbols:
            val = expr.evalf(20)
            if val.is_real and val.is_finite:
                return str(val)
    except Exception:
        pass

    return ans


def post_process(response, is_mcq=False):
    try:
        ans_list = extract_ANS(response)
        if not ans_list:
            return response

        new_box = ", ".join(coerce(ans, is_mcq=is_mcq) for ans in ans_list)
        if new_box.strip() == judger.extract_boxed_answer(response).strip():
            return response
        return response.rstrip() + f"\n\nCoerced final answer: \\boxed{{{new_box}}}"
    except Exception:
        return response

